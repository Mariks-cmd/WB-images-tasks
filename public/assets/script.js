$('#image_name').on('submit', function(event) {
  event.preventDefault();
  let url = $(this).attr('action'),
      data = $(this).serialize();

  $.post(url, data).done(function (response) {
    if (response.status) {
      $('.main_image').attr('src', response.url);
    }
  });
 
})